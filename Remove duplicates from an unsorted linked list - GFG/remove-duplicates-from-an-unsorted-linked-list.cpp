class Solution
{
    public:
    //Function to remove duplicates from unsorted linked list.
    Node * removeDuplicates( Node *head) 
    {
        set<int> s;
        Node *st=head,*p=head;
        
        while(p){
            if(s.size()==0){
                s.insert(p->data);
                p=p->next;
                if(p==NULL) break;
            }
            
            if(s.find(p->data)==s.end()){
                s.insert(p->data);
                st->next=p;
                st=p;
                p=p->next;
            }
            else p=p->next;
        }
        st->next=NULL;
        return head;
    }
};


