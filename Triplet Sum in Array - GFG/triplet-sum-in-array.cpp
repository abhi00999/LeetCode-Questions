class Solution{
    public:
    //Function to find if there exists a triplet in the 
    //array A[] which sums up to X.
    bool find3Numbers(int A[], int n, int X)
    {
        //Your Code Here
        sort(A,A+n);
        for(int i=0;i<n;i++){
            int ext=X-A[i];
            int left=i+1,right=n-1;
            if(ext<0) continue;
            
            while(left<right){
                int sum=A[left]+A[right];
                if(sum==ext) return true;
                else if(sum<ext) left++;
                else right--;
            }
        }
        return false;
    }

};

